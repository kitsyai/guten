import { mkdirSync, readFileSync, writeFileSync, existsSync } from 'node:fs';
import { join, resolve } from 'node:path';

const repoRoot = resolve(process.cwd());
const templatesDir = join(repoRoot, 'templates', 'internal');
const goOut = join(repoRoot, 'go', 'builtins.generated.go');
const jsOut = join(repoRoot, 'js', 'src', 'builtins.generated.ts');
const cliOut = join(repoRoot, 'cli', 'internal', 'library', 'builtins.generated.go');

const indexPath = join(templatesDir, 'index.json');
const index = JSON.parse(readFileSync(indexPath, 'utf8'));

function readJSON(filePath) {
  return JSON.parse(readFileSync(filePath, 'utf8'));
}

function readOptionalJSON(filePath) {
  if (!existsSync(filePath)) return null;
  return readJSON(filePath);
}

function parseTemplate(entry) {
  const dir = join(templatesDir, entry.path);
  const manifest = readJSON(join(dir, 'template.json'));
  const parts = {};
  for (const [part, source] of Object.entries(manifest.parts || {})) {
    parts[part] = source.startsWith('@') ? readFileSync(join(dir, source.slice(1)), 'utf8') : source;
  }
  return {
    name: manifest.name || entry.name,
    kind: manifest.kind || entry.kind || 'email',
    description: manifest.description || entry.description || '',
    renderer: manifest.renderer || 'liquid',
    extends: manifest.extends || '',
    parts,
    theme: readOptionalJSON(join(dir, 'theme.json')),
    sample: readOptionalJSON(join(dir, 'sample.json')),
  };
}

const resolved = index.templates.map(parseTemplate);

const goPayload = JSON.stringify(
  resolved.map((item) => {
    const { theme, sample, ...template } = item;
    return template;
  })
);

const goBody = `// Code generated by scripts/gen-guten-builtins.mjs; DO NOT EDIT.

package guten

import (
  "encoding/json"
  "fmt"
)

const builtinTemplatesJSON = ${JSON.stringify(goPayload)}

func builtinTemplates() []Template {
  var out []Template
  if err := json.Unmarshal([]byte(builtinTemplatesJSON), &out); err != nil {
    panic(fmt.Sprintf("guten: parse embedded builtins JSON: %v", err))
  }
  return out
}
`;

const jsPayload = JSON.stringify(
  resolved.map((item) => {
    const { theme, sample, ...template } = item;
    return template;
  }),
  null,
  2
);
const jsBody = `// Code generated by scripts/gen-guten-builtins.mjs; DO NOT EDIT.

export interface BuiltinTemplateSpec {
  name: string;
  kind?: string;
  renderer?: string;
  extends?: string;
  description?: string;
  parts: Record<string, string>;
}

export const builtinTemplates: BuiltinTemplateSpec[] = ${jsPayload};
`;

const cliPayload = JSON.stringify(resolved, null, 2);
const cliBody = `// Code generated by scripts/gen-guten-builtins.mjs; DO NOT EDIT.

package library

import (
  "encoding/json"
  "fmt"
)

type builtinTemplateSpec struct {
  Name        string ` + "`" + `json:"name"` + "`" + `
  Kind        string ` + "`" + `json:"kind"` + "`" + `
  Description string ` + "`" + `json:"description"` + "`" + `
  Renderer    string ` + "`" + `json:"renderer"` + "`" + `
  Extends     string ` + "`" + `json:"extends"` + "`" + `
  Parts       map[string]string ` + "`" + `json:"parts"` + "`" + `
  Theme       json.RawMessage ` + "`" + `json:"theme"` + "`" + `
  Sample      json.RawMessage ` + "`" + `json:"sample"` + "`" + `
}

var builtinTemplateCatalogJSON = ${JSON.stringify(cliPayload)}

var builtinTemplateCatalog []builtinTemplateSpec

func init() {
  if err := json.Unmarshal([]byte(builtinTemplateCatalogJSON), &builtinTemplateCatalog); err != nil {
    panic(fmt.Sprintf("cli/library: parse builtin template catalog: %v", err))
  }
}
`;

mkdirSync(join(repoRoot, 'js', 'src'), { recursive: true });
mkdirSync(join(repoRoot, 'go'), { recursive: true });
mkdirSync(join(repoRoot, 'cli', 'internal', 'library'), { recursive: true });
writeFileSync(goOut, goBody, 'utf8');
writeFileSync(jsOut, jsBody, 'utf8');
writeFileSync(cliOut, cliBody, 'utf8');
